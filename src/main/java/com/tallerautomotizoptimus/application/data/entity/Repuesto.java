package com.tallerautomotizoptimus.application.data.entity;

public class Repuesto {

	
	private int idrepuesto;
	private String nombre;
	private String numeroserie;
	private int cantidad;
	private Double preciocosto;
	private Double precio;
	private String aniovehiculo;
	private String compvehiculo;
	private String marca;
	private String clasificacion;
	private String fabricante;
	
	
	public int getIdrepuesto() {
		return idrepuesto;
	}
	public void setIdrepuesto(int idrepuesto) {
		this.idrepuesto = idrepuesto;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNumeroserie() {
		return numeroserie;
	}
	public void setNumeroserie(String numeroserie) {
		this.numeroserie = numeroserie;
	}
	public int getCantidad() {
		return cantidad;
	}
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	public Double getPreciocosto() {
		return preciocosto;
	}
	public void setPreciocosto(Double preciocosto) {
		this.preciocosto = preciocosto;
	}
	public Double getPrecio() {
		return precio;
	}
	public void setPrecio(Double precio) {
		this.precio = precio;
	}
	public String getAniovehiculo() {
		return aniovehiculo;
	}
	public void setAniovehiculo(String aniovehiculo) {
		this.aniovehiculo = aniovehiculo;
	}
	public String getCompvehiculo() {
		return compvehiculo;
	}
	public void setCompvehiculo(String compvehiculo) {
		this.compvehiculo = compvehiculo;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getClasificacion() {
		return clasificacion;
	}
	public void setClasificacion(String clasificacion) {
		this.clasificacion = clasificacion;
	}
	public String getFabricante() {
		return fabricante;
	}
	public void setFabricante(String fabricante) {
		this.fabricante = fabricante;
	}
	
	
	
	
}
